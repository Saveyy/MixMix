    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            using (ServiceHost host = new ServiceHost(typeof(Service)))
            {
                bool hostOpen = false;
                host.Open();
                hostOpen = true;
                while(hostOpen)
                {
                    Thread.Sleep(1000);
                }
            }
        }
    }
